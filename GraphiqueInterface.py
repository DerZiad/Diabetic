    def validFirstPanel(self):
        valid = True
        pregnanciesEdit = self.pregnanciesEdit.text().strip()
        SkinThicknessEdit = self.skinThicknessEdit.text().strip()
        DiabetesPedigreeFunctionEdit = self.diabetesPedigreeFunctionEdit.text().strip()
        BloodPressureEdit = self.bloodPresureEdit.text().strip()
        AgeEdit = self.ageEdit.text().strip()
        BMIEdit = self.bmiEdit.text().strip()
        InsulinEdit = self.insulinEdit.text().strip()
        GlucoseEdit = self.glucoseEdit.text().strip()

        try:
            pregnanciesEdit = float(pregnanciesEdit)
        except ValueError:
            valid = False
            self.pregnanciesError.setText("Valeur numérique")

        try:
            SkinThicknessEdit = float(SkinThicknessEdit)
        except ValueError:
            valid = False
            self.skinThicknessError.setText("Valeur numérique")

        try:
            DiabetesPedigreeFunctionEdit = float(DiabetesPedigreeFunctionEdit)
        except ValueError:
            valid = False
            self.diabetesPedigreeFunctionError.setText("Valeur numérique")

        try:
            BloodPressureEdit = float(BloodPressureEdit)
        except ValueError:
            valid = False
            self.bloodPresureError.setText("Valeur numérique")

        try:
            AgeEdit = float(AgeEdit)
        except ValueError:
            valid = False
            self.ageError.setText("Valeur numérique")

        try:
            BMIEdit = float(BMIEdit)
        except ValueError:
            valid = False
            self.bmiError.setText("Valeur numérique")

        try:
            InsulinEdit = float(InsulinEdit)
        except ValueError:
            valid = False
            self.insulinError.setText("Valeur numérique")

        try:
            GlucoseEdit = float(GlucoseEdit)
        except ValueError:
            valid = False
            self.glucoseError.setText("Valeur numérique")
        checkBoxMachineLearning = False
        checkBoxDeepLearning = False
        if self.machineLearningCheckbox.isChecked():
            checkBoxMachineLearning = True

        if self.deepLearningCheckbox.isChecked():
            checkBoxDeepLearning = True

        if checkBoxDeepLearning == False and checkBoxDeepLearning == False:
            self.typeError.setText("Selectionnez un modele")

        return valid, pregnanciesEdit, SkinThicknessEdit, DiabetesPedigreeFunctionEdit, BloodPressureEdit, AgeEdit, BMIEdit, InsulinEdit, GlucoseEdit,checkBoxDeepLearning,checkBoxMachineLearning

    def clicked(self):
        self.refreshFisrtPanel()
        valid, pregnanciesEdit, SkinThicknessEdit, DiabetesPedigreeFunctionEdit, BloodPressureEdit, AgeEdit, BMIEdit, InsulinEdit, GlucoseEdit,deepLearning,machineLearning = self.validFirstPanel()
        if valid:
            x = [[pregnanciesEdit, GlucoseEdit, BloodPressureEdit, SkinThicknessEdit, InsulinEdit, BMIEdit, DiabetesPedigreeFunctionEdit, AgeEdit]]
            if deepLearning:
                predictionsDeepLearning = self.models.predictDeepLearning(x)
                if predictionsDeepLearning == 0:
                    self.predictionDeepLearning.setText("La personne est non diabétique")
                else:
                    self.predictionDeepLearning.setText("La personne est diabetique")
            if machineLearning:
                predictionsMachineLearning = self.models.predictMachineLearning(x)
                if predictionsMachineLearning == 0:
                    self.predictionMachineLearning.setText("La personne est non diabétique")
                else:
                    self.predictionMachineLearning.setText("La personne est diabétique")
