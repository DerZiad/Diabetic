    def clickPredictXLSX(self):
        self.typeError2.setText("")
        if self.deepLearningBrowse.isChecked() and self.machineLearningImport.isChecked():
            self.typeError2.setText("Only one must be selected")
        elif (not self.deepLearningBrowse.isChecked()) and (not self.machineLearningImport.isChecked()):
            self.typeError2.setText("You must select one")
        else:
            deeper = GoDeepInFile(self.labelImport.text())
